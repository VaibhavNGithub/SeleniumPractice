a = 37
b = 4

# Addition Operator (+)

print('Arithmetic Addition Operator:       {} + {} = '.format(a,b),a + b)

# Subtraction Operator (-)

print('Arithmetic Subtraction Operator:   {} - {} = '.format(a,b),a - b)

# Multiplication Operator (*)

print('Arithmetic Multiplication Operator: {} * {} = '.format(a,b),a * b)

# Division Operator (/)

print('Arithmetic Division Operator:       {} / {} = '.format(a,b),a / b)

# Floor Division Operator (//)

print('Arithmetic Floor Division Operator: {} // {} = '.format(a,b),a // b)

# Modulus Operator (%)

print('Arithmetic Modulus Operator:        {} % {} = '.format(a,b),a % b)

# Exponent Operator (**)

print('Arithmetic Exponent Operator:       {} ** {} = '.format(a,b),a ** b)